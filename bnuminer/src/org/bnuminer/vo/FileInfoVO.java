package org.bnuminer.vo;

public class FileInfoVO {
	private int file_id;
	private String file_name;
	private String file_create_time;
	private String file_modify_time;
	private int file_size;
	private String file_type;
	
	
	public int getFile_id() {
		return file_id;
	}
	public void setFile_id(int fileId) {
		file_id = fileId;
	}
	
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String fileName) {
		file_name = fileName;
	}
	
	public String getFile_create_time() {
		return file_create_time;
	}
	public void setFile_create_time(String fileCreateTime) {
		file_create_time = fileCreateTime;
	}
	public String getFile_modify_time() {
		return file_modify_time;
	}
	public void setFile_modify_time(String fileModifyTime) {
		file_modify_time = fileModifyTime;
	}
	public int getFile_size() {
		return file_size;
	}
	public void setFile_size(int fileSize) {
		file_size = fileSize;
	}
	public String getFile_type() {
		return file_type;
	}
	public void setFile_type(String fileType) {
		file_type = fileType;
	}
	
	
	
	
}
