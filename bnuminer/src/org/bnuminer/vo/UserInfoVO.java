package org.bnuminer.vo;

public class UserInfoVO {
	private int user_id;
	private String user_name;
	private String user_pwd;
	private String user_true_name;
	private String user_email;
	private String user_pri;
	private String user_signup;
	
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int userId) {
		user_id = userId;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String userName) {
		user_name = userName;
	}
	public String getUser_pwd() {
		return user_pwd;
	}
	public void setUser_pwd(String userPwd) {
		user_pwd = userPwd;
	}
	public String getUser_true_name() {
		return user_true_name;
	}
	public void setUser_true_name(String userTrueName) {
		user_true_name = userTrueName;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String userEmail) {
		user_email = userEmail;
	}
	public String getUser_pri() {
		return user_pri;
	}
	public void setUser_pri(String userPri) {
		user_pri = userPri;
	}
	public String getUser_signup() {
		return user_signup;
	}
	public void setUser_signup(String userSignup) {
		user_signup = userSignup;
	}
	
	
}
